            jTextField7.setText(SALARY);
            

            String main="STAFFID: " +STAFFID+ "\nSTAFFNAME: " +STAFFNAME+ "\nSPECIALIZATION: " +SPECIALIZATION+ "\nEMAILID: " +EMAILID+ "\nCONTACTNUMBER: " +CONTACTNUMBER+ "\nJOININGDATE: " +JOININGDATE+ "\nSALARY: " +SALARY;
            if (count==0)
            {
                JOptionPane.showMessageDialog(this,"no data found");
            }
            jTextArea1.setText(main);

            if(jTextField1.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this,"no data found");
            }
            con.close();
            st.close();

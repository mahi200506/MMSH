    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        
         JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Select Employee Image");
    
    // Allow only image files
    FileNameExtensionFilter filter = new FileNameExtensionFilter("Image Files", "jpg", "jpeg", "png", "gif");
    fileChooser.setFileFilter(filter);

    int result = fileChooser.showOpenDialog(this);

    if (result == JFileChooser.APPROVE_OPTION) {
        File selectedFile = fileChooser.getSelectedFile();

        try {
            // Display image in JLabel
            ImageIcon icon = new ImageIcon(selectedFile.getAbsolutePath());
            Image img = icon.getImage().getScaledInstance(jLabel10.getWidth(), jLabel10.getHeight(), Image.SCALE_SMOOTH);
            jLabel10.setIcon(new ImageIcon(img));

            // Create destination folder if not exists
            File destFolder = new File("STAFF_PIC");
            if (!destFolder.exists()) {
                destFolder.mkdirs();
            }

            // Save the image to "saved_images" folder
           // String fileName = selectedFile.getName();
            String fileName = jTextField1.getText();
            File destination = new File(destFolder, fileName);

            // Copy the file
            Files.copy(selectedFile.toPath(), destination.toPath(), StandardCopyOption.REPLACE_EXISTING);

            JOptionPane.showMessageDialog(this, "Image saved to: " + destination.getAbsolutePath());

        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saving image: " + e.getMessage());
        }
    }


 
    }//GEN-LAST:event_jButton8ActionPerformed

